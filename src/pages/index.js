// constant
const educationContainerNonactive = document.querySelector('.education__container_nonactive');
const educationButtonMagistr = document.querySelector('.education__button-magistr');
const educationButtonAspirant = document.querySelector('.education__button-aspirant');
const educationButton = document.querySelectorAll('.education__button');

//change card content 
educationButton.forEach(function(button){
  button.addEventListener ('click', function(){
    if (button.classList.contains('education__button-magistr'))
    {
      educationButtonMagistr.classList.add('education__button_active');
      educationButtonAspirant.classList.remove('education__button_active');
      educationContainerNonactive.classList.add('education__container_active');
    };
    if (button.classList.contains('education__button-aspirant'))
    {
      educationButtonMagistr.classList.remove('education__button_active');
      educationButtonAspirant.classList.add('education__button_active');
      educationContainerNonactive.classList.remove('education__container_active');
    };
  })});



  

  

  
